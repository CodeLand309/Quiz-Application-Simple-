    public void loadData(String num, String sub) throws SQLException
    {
        DB.Connect();
        String qry="select number, question, a, b, c, d, subject, answer from question where number=? and subject=?";
        PreparedStatement smt=DB.conn.prepareStatement(qry);
        smt.setString(1, num);
        smt.setString(2, sub);
        ResultSet rs=smt.executeQuery();
        Vector data=new Vector();
        while(rs.next())
        {
            Vector row=new Vector();
            row.add(rs.getString(1));
            row.add(rs.getString(2));
            row.add(rs.getString(3));
            row.add(rs.getString(4));
            row.add(rs.getString(5));
            row.add(rs.getString(6));
            row.add(rs.getString(7));
            row.add(rs.getString(8));
            data.add(row);
        }
        DB.disconnect();
        //DefaultTableModel model=new DefaultTableModel(data,head);
        //mytable.setModel(model);
        //mytable.setVisible(false);
        //ans.requestFocus(true);
        Vector detail=(Vector)data.elementAt(0);
        //number.setText(i+"");
        number.setText(detail.elementAt(0).toString());
        qn.setText(detail.elementAt(1).toString());
        a.setText(detail.elementAt(2).toString());
        b.setText(detail.elementAt(3).toString());
        c.setText(detail.elementAt(4).toString());
        d.setText(detail.elementAt(5).toString());
        subject.setText(detail.elementAt(6).toString());
        answer.setText(detail.elementAt(7).toString());
    }
