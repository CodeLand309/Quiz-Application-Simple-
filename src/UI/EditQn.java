    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try
        {
            String nnum=number.getText();
            String nqn=qn.getText();
            String na=a.getText();
            String nb=b.getText();
            String nc=c.getText();
            String nd=d.getText();
            String nsub=subject.getText();
            String nanswer=answer.getText();
            if(model.edit(nnum, nqn, na, nb, nc, nd, nsub, nanswer, num, sub))
            {
                JOptionPane.showMessageDialog(null, "Question Modified", "Success", 1);
                this.dispose();
                new Question().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Failed to Edit questions", "Failed", 2);
                this.dispose();
                new Question().setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(OnlineQuiz.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (ArrayIndexOutOfBoundsException ex)
        {
            JOptionPane.showMessageDialog(null, "Question number or subject Incorrect", "Failed", 0);
        }
    }//GEN-LAST:event_addActionPerformed

