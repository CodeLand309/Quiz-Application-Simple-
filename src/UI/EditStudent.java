    public void loadData(String upass) throws SQLException
    {
        DB.Connect();
        String qry="select name, school, uname, pass from user where pass=?";
        PreparedStatement smt=DB.conn.prepareStatement(qry);
        smt.setString(1, upass);
        ResultSet rs=smt.executeQuery();
        Vector data=new Vector();
        while(rs.next())
        {
            Vector row=new Vector();
            row.add(rs.getString(1));
            row.add(rs.getString(2));
            row.add(rs.getString(3));
            row.add(rs.getString(4));
            data.add(row);
        }
        DB.disconnect();
        //DefaultTableModel model=new DefaultTableModel(data,head);
        //mytable.setModel(model);
        //mytable.setVisible(false);
        //ans.requestFocus(true);
        Vector detail=(Vector)data.elementAt(0);
        //number.setText(i+"");
        name.setText(detail.elementAt(0).toString());
        school.setText(detail.elementAt(1).toString());
        user.setText(detail.elementAt(2).toString());
        pass.setText(detail.elementAt(3).toString());
    }
