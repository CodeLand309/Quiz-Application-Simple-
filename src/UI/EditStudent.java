    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            String nname=name.getText();
            String nschool=school.getText();
            String nuser=user.getText();
            String npass=pass.getText();
            if(model.editUser(nname,nschool,nuser,npass,upass))
            {
                JOptionPane.showMessageDialog(null, "Profile Edited", "Success",1);
                this.dispose();
                new StudDash(nuser,npass).setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Failed to edit user details", "Failed",2);
                this.dispose();
                new StudDash(nuser,npass).setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

